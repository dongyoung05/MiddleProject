package kr.or.ddit.vo;

public class ReplyVO {
	
	private String reply_no;
	private String reply_content;
	private String reply_writer;
	private String reply_date;
	private String reply_delyn;
	private String board_no;
	
	public String getReply_no() {
		return reply_no;
	}
	public void setReply_no(String reply_no) {
		this.reply_no = reply_no;
	}
	public String getReply_content() {
		return reply_content;
	}
	public void setReply_content(String reply_content) {
		this.reply_content = reply_content;
	}
	public String getReply_writer() {
		return reply_writer;
	}
	public void setReply_writer(String reply_writer) {
		this.reply_writer = reply_writer;
	}
	public String getReply_date() {
		return reply_date;
	}
	public void setReply_date(String reply_date) {
		this.reply_date = reply_date;
	}
	public String getReply_delyn() {
		return reply_delyn;
	}
	public void setReply_delyn(String reply_delyn) {
		this.reply_delyn = reply_delyn;
	}
	public String getBoard_no() {
		return board_no;
	}
	public void setBoard_no(String board_no) {
		this.board_no = board_no;
	}
	@Override
	public String toString() {
		return "ReplyVO [reply_no=" + reply_no + ", reply_content=" + reply_content + ", reply_writer=" + reply_writer
				+ ", reply_date=" + reply_date + ", reply_delyn=" + reply_delyn + ", board_no=" + board_no + "]";
	}
	
	

}
