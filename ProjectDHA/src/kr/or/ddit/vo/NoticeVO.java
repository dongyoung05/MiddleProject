package kr.or.ddit.vo;

public class NoticeVO {
	
	private String notice_no;
	private String notice_title;
	private String notice_content;
	private String notice_date;
	private String notice_img;
	private String notice_delyn;
	private String admin_id;
	public String getNotice_no() {
		return notice_no;
	}
	public void setNotice_no(String notice_no) {
		this.notice_no = notice_no;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_date() {
		return notice_date;
	}
	public void setNotice_date(String notice_date) {
		this.notice_date = notice_date;
	}
	public String getNotice_img() {
		return notice_img;
	}
	public void setNotice_img(String notice_img) {
		this.notice_img = notice_img;
	}
	public String getNotice_delyn() {
		return notice_delyn;
	}
	public void setNotice_delyn(String notice_delyn) {
		this.notice_delyn = notice_delyn;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	@Override
	public String toString() {
		return "NoticeVO [notice_no=" + notice_no + ", notice_title=" + notice_title + ", notice_content="
				+ notice_content + ", notice_date=" + notice_date + ", notice_img=" + notice_img + ", notice_delyn="
				+ notice_delyn + ", admin_id=" + admin_id + "]";
	}
	
	

}
